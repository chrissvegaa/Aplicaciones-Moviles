package com.example.fragmentos;

public class operaciones {
    private int dato;
    public int getDato() {
        return dato;
    }

    public void setDato(int dato) {
        this.dato = dato;
    }
    public int doble(){
        return dato * 2;
    }
    public int triple(){
        return dato * 3;
    }
    public int cuadruple(){
        return dato * 4;
    }

}
